import ymd


def test_date_list():
    assert ymd.date_list("2024-01-01", "2024-01-05") == [
        "2024-01-01",
        "2024-01-02",
        "2024-01-03",
        "2024-01-04",
    ]
    assert ymd.date_list("2024-01-01", 4) == [
        "2024-01-01",
        "2024-01-02",
        "2024-01-03",
        "2024-01-04",
    ]
    assert ymd.date_list("2024-01-05", "2024-01-01", -1) == [
        "2024-01-05",
        "2024-01-04",
        "2024-01-03",
        "2024-01-02",
    ]
    assert ymd.date_list("2024-01-05", 4, -1) == [
        "2024-01-05",
        "2024-01-04",
        "2024-01-03",
        "2024-01-02",
    ]
    assert ymd.date_list("2024-01-01", 5, 2) == [
        "2024-01-01",
        "2024-01-03",
        "2024-01-05"
    ]
    assert ymd.date_list("2024-01-01", 6, 2) == [
        "2024-01-01",
        "2024-01-03",
        "2024-01-05"
    ]
